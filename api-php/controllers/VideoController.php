            return [
                "status" => 200,
                "message" => "V√≠deo encontrado com sucesso.",
                "data" => $video
            ];
        } catch (Exception $e) {
            return [
                "status" => 500,
                "message" => "Erro ao buscar v√≠deo.",
                "error" => $e->getMessage()
            ];
        }
    }
