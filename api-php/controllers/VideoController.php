            $stmt->execute();
            $video = $stmt->fetch(PDO::FETCH_ASSOC);

            if (!$video) {
                return ['status' => 404, 'message' => 'V√≠deo n√£o encontrado'];
            }

            // Formata tamanho em MB
            $video['tamanho_mb'] = round($video['tamanho_bytes'] / (1024 * 1024), 2);
